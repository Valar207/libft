int		ft_is_prime(int nb)
{
	int	i;
	int	count;

	i = 2;
	count = 1;
	if (nb < 2)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			count = 0;
		i++;
	}
	if (count == 1)
		return (1);
	else
		return (0);
}
